public class Variaveis
{
    public static void main(String[] args) {
        int varInteiro = 7;
        float varReal = 1.83f;
        double varRDouble = 3.1415;
        char varChar = 'K';
        String varCadeia = "Variaveis"; 

        System.out.printf("%d\n", varInteiro);
        System.out.printf("%.2f\n", varReal);
        System.out.printf("%.4f\n", varRDouble);
        System.out.printf("%c\n", varChar);
        System.out.printf("%s\n", varCadeia);
    }
}